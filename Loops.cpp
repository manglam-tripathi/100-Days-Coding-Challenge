#include <iostream> 
using namespace std; 

int main() 
{ 
// 	Need of loop
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	cout << "Hello World\n"; 
	
	
	
	//for loop
	for (int i = 1; i <= 10; i++) 
    { 
        cout << "Hello World\n"; 
    } 
    
    
   //while loop
    int i = 1; 
  
  
    while (i < 6) 
    { 
        cout << "Hello World\n"; 
  
        i++; 
    } 
  
  
  
  //do while loop
  
    do
    { 
        // loop body 
        cout << "Hello World\n"; 
  
        // update expression 
        i++; 
  
    }  while (i < 1);   // test expression 
	return 0; 
} 

